package game.enums;

/**
 * Use this enum class to give `buff` or `debuff`.
 * It is also useful to give a `state` to abilities or actions that can be attached-detached.
 */
public enum Status {
    HOSTILE_TO_ENEMY,
    HOSTILE_TO_PLAYER,
    FULLY_CHARGED,
    DISARMED,
    HOLD,
    STUN,
    FALLEN,
    AGGRESSIVE,
    DULLNESS,
    RESET,
    MISS_ATTACK,
    BURNED,
    LIT,
    HEAVY_ATTACK,
}
